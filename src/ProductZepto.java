public class ProductZepto {

    private int id;
    private String name;
    private double mrp;
    private double sp;
    private boolean isGdp;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getMrp() {
        return mrp;
    }

    public void setMrp(double mrp) {
        this.mrp = mrp;
    }

    public double getSp() {
        return sp;
    }

    public void setSp(double sp) {
        this.sp = sp;
    }

    public boolean isGdp() {
        return isGdp;
    }

    public void setGdp(boolean gdp) {
        isGdp = gdp;
    }
}
